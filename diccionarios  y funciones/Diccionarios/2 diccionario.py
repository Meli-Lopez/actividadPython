Diccionario = {'nombre': 'Sandra', 'edad': 44}
# Accediendo a los elementos de un diccionario: 
print(Diccionario['nombre']) # Imprime el valor asociado a la clave 'nombre' en el diccionario.
print(Diccionario.get('nombre', 'No existe')) # Imprime el valor asociado a la clave 'nombre' en el diccionario, si no existe, imprime 'No existe'.
# Get: se utiliza para acceder a los valores de un diccionario 