#Funcion con parametros: 
# Definir una funcion llamada 'raiz' que toma un argumento llamado 'value'.
def raiz(value):
    # La funcion devuelve el resultado de elevar 'value' a la potencia 1/2, que es la raiz cuadrada.
    return value ** (1/2)
# Imprime un mensaje que incluye el resultado de llamar a la funcion y el argumento 4.
print(f'La raiz cuadrada es: {raiz(4)}')